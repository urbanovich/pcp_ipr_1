    private void removeRow() {
        DefaultTableModel model = (DefaultTableModel) this.jTable2.getModel();
        
        for(int i = (model.getRowCount() - 1);  i != -1; i--) {
            boolean val = (boolean)model.getValueAt(i, 0);
            if (val) {
                model.removeRow(i);
                this.map.remove(i);
            }
        }
    }
    
