    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        DefaultTableModel model = (DefaultTableModel) this.jTable2.getModel();
                
        //before searching to need to remove all rows from the table
        for(int i = (model.getRowCount() - 1);  i != -1; i--) {
            model.removeRow(i);
        }
        
        //now to search a plant by text data from fields
        for (BotanicalGarden plant: this.map.values()) {
            
            if (plant.title.equals(this.title.getText()) || 
                plant.family.equals(this.family.getText()) || 
                plant.genus.equals(this.genus.getText()) || 
                plant.type.equals(this.type.getText()) || 
                plant.age.equals(this.age.getText()) 
                    ) {
                
                model.addRow(new Object [] {
                    null,
                    plant.title, 
                    plant.family, 
                    plant.genus, 
                    plant.type, 
                    plant.age
                });
            }
            
        }
        
        this.initTable();
        
    }//GEN-LAST:event_jButton3MouseClicked

